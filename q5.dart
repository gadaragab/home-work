/*Exercise: Write a program that checks the role of a user (e.g., admin, user, guest). Print 'Welcome Admin' if the role is
admin, 'Welcome User' if it's user, and 'Welcome Guest' otherwise.*/
void main() {
  bool isAdmin = false;
  bool isUser = false;
  if (isAdmin) {
    print('Welcome User');
  } else if (isUser) {
    print('Welcome User');
  } else {
    print('Welcome Guest');
  }
}
